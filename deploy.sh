#!/bin/bash

# –°–∫—Ä–∏–ø—Ç –¥–ª—è –±—ã—Å—Ç—Ä–æ–≥–æ –¥–µ–ø–ª–æ—è –Ω–∞ —Å–µ—Ä–≤–µ—Ä
# –ò—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ: ./deploy.sh [production|staging]

set -e

# –¶–≤–µ—Ç–∞ –¥–ª—è –≤—ã–≤–æ–¥–∞
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
NC='\033[0m' # No Color

# –ö–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏—è
SERVER_HOST="217.198.6.238"
SERVER_USER="root"
SSH_KEY="$HOME/.ssh/timeweb_vps_key"
SSH_PORT="22"

# –û–ø—Ä–µ–¥–µ–ª—è–µ–º –æ–∫—Ä—É–∂–µ–Ω–∏–µ
ENVIRONMENT=${1:-production}

if [[ "$ENVIRONMENT" != "production" && "$ENVIRONMENT" != "staging" ]]; then
    echo -e "${RED}–û—à–∏–±–∫–∞: –£–∫–∞–∂–∏—Ç–µ –æ–∫—Ä—É–∂–µ–Ω–∏–µ (production –∏–ª–∏ staging)${NC}"
    echo "–ò—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ: ./deploy.sh [production|staging]"
    exit 1
fi

echo -e "${GREEN}üöÄ –ù–∞—á–∏–Ω–∞–µ–º –¥–µ–ø–ª–æ–π –≤ –æ–∫—Ä—É–∂–µ–Ω–∏–µ: $ENVIRONMENT${NC}"

# –ü—Ä–æ–≤–µ—Ä—è–µ–º SSH –∫–ª—é—á
if [ ! -f "$SSH_KEY" ]; then
    echo -e "${RED}–û—à–∏–±–∫–∞: SSH –∫–ª—é—á –Ω–µ –Ω–∞–π–¥–µ–Ω: $SSH_KEY${NC}"
    exit 1
fi

# –°–±–æ—Ä–∫–∞ –ø—Ä–æ–µ–∫—Ç–∞
echo -e "${YELLOW}üì¶ –°–æ–±–∏—Ä–∞–µ–º –ø—Ä–æ–µ–∫—Ç...${NC}"
pnpm install --frozen-lockfile
pnpm run build

# –°–æ–∑–¥–∞–µ–º –≤—Ä–µ–º–µ–Ω–Ω—É—é –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—é
TEMP_DIR="/tmp/supermock-ai-interview-$(date +%s)"
echo -e "${YELLOW}üìÅ –°–æ–∑–¥–∞–µ–º –≤—Ä–µ–º–µ–Ω–Ω—É—é –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—é: $TEMP_DIR${NC}"

# –ö–æ–ø–∏—Ä—É–µ–º —Ñ–∞–π–ª—ã –Ω–∞ —Å–µ—Ä–≤–µ—Ä
echo -e "${YELLOW}üì§ –ö–æ–ø–∏—Ä—É–µ–º —Ñ–∞–π–ª—ã –Ω–∞ —Å–µ—Ä–≤–µ—Ä...${NC}"
ssh -i "$SSH_KEY" -p "$SSH_PORT" "$SERVER_USER@$SERVER_HOST" "mkdir -p $TEMP_DIR"

# –ö–æ–ø–∏—Ä—É–µ–º –≤—Å–µ —Ñ–∞–π–ª—ã –∫—Ä–æ–º–µ node_modules –∏ .git
rsync -avz --exclude 'node_modules' --exclude '.git' --exclude 'dist' \
    -e "ssh -i $SSH_KEY -p $SSH_PORT" \
    ./ "$SERVER_USER@$SERVER_HOST:$TEMP_DIR/"

# –û–ø—Ä–µ–¥–µ–ª—è–µ–º –Ω–∞—Å—Ç—Ä–æ–π–∫–∏ –¥–ª—è –æ–∫—Ä—É–∂–µ–Ω–∏—è
if [[ "$ENVIRONMENT" == "production" ]]; then
    CONTAINER_NAME="supermock-ai-interview"
    PORT="3000"
    DIRECTORY="/var/www/supermock-ai-interview"
else
    CONTAINER_NAME="supermock-ai-interview-staging"
    PORT="8080"
    DIRECTORY="/var/www/supermock-ai-interview-staging"
fi

# –í—ã–ø–æ–ª–Ω—è–µ–º –¥–µ–ø–ª–æ–π –Ω–∞ —Å–µ—Ä–≤–µ—Ä–µ
echo -e "${YELLOW}üîß –í—ã–ø–æ–ª–Ω—è–µ–º –¥–µ–ø–ª–æ–π –Ω–∞ —Å–µ—Ä–≤–µ—Ä–µ...${NC}"
ssh -i "$SSH_KEY" -p "$SSH_PORT" "$SERVER_USER@$SERVER_HOST" << EOF
    # –û—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º —Ç–µ–∫—É—â–∏–π –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä
    docker stop $CONTAINER_NAME 2>/dev/null || true
    docker rm $CONTAINER_NAME 2>/dev/null || true
    
    # –£–¥–∞–ª—è–µ–º —Å—Ç–∞—Ä—ã–π –æ–±—Ä–∞–∑
    docker rmi $CONTAINER_NAME:latest 2>/dev/null || true
    
    # –°–æ–∑–¥–∞–µ–º –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—é –¥–ª—è –ø—Ä–æ–µ–∫—Ç–∞
    mkdir -p $DIRECTORY
    
    # –ö–æ–ø–∏—Ä—É–µ–º —Ñ–∞–π–ª—ã –∏–∑ –≤—Ä–µ–º–µ–Ω–Ω–æ–π –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏
    cp -r $TEMP_DIR/* $DIRECTORY/
    
    # –ü–µ—Ä–µ—Ö–æ–¥–∏–º –≤ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—é –ø—Ä–æ–µ–∫—Ç–∞
    cd $DIRECTORY
    
    # –°–æ–±–∏—Ä–∞–µ–º Docker –æ–±—Ä–∞–∑
    docker build -t $CONTAINER_NAME:latest .
    
    # –ó–∞–ø—É—Å–∫–∞–µ–º –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä
    docker run -d \
        --name $CONTAINER_NAME \
        --restart unless-stopped \
        -p $PORT:80 \
        $CONTAINER_NAME:latest
    
    # –û—á–∏—â–∞–µ–º –≤—Ä–µ–º–µ–Ω–Ω—É—é –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—é
    rm -rf $TEMP_DIR
    
    # –û—á–∏—â–∞–µ–º –Ω–µ–∏—Å–ø–æ–ª—å–∑—É–µ–º—ã–µ –æ–±—Ä–∞–∑—ã
    docker image prune -f
EOF

# –ü—Ä–æ–≤–µ—Ä—è–µ–º —Å—Ç–∞—Ç—É—Å
echo -e "${YELLOW}üîç –ü—Ä–æ–≤–µ—Ä—è–µ–º —Å—Ç–∞—Ç—É—Å –¥–µ–ø–ª–æ—è...${NC}"
sleep 5

# –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä –∑–∞–ø—É—â–µ–Ω
CONTAINER_STATUS=$(ssh -i "$SSH_KEY" -p "$SSH_PORT" "$SERVER_USER@$SERVER_HOST" "docker ps --filter name=$CONTAINER_NAME --format '{{.Status}}'")

if [[ -n "$CONTAINER_STATUS" ]]; then
    echo -e "${GREEN}‚úÖ –ö–æ–Ω—Ç–µ–π–Ω–µ—Ä —É—Å–ø–µ—à–Ω–æ –∑–∞–ø—É—â–µ–Ω!${NC}"
    echo -e "${GREEN}üåê –ü—Ä–∏–ª–æ–∂–µ–Ω–∏–µ –¥–æ—Å—Ç—É–ø–Ω–æ –ø–æ –∞–¥—Ä–µ—Å—É: http://$SERVER_HOST:$PORT${NC}"
    echo -e "${GREEN}üìä –°—Ç–∞—Ç—É—Å –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä–∞: $CONTAINER_STATUS${NC}"
else
    echo -e "${RED}‚ùå –û—à–∏–±–∫–∞: –ö–æ–Ω—Ç–µ–π–Ω–µ—Ä –Ω–µ –∑–∞–ø—É—â–µ–Ω${NC}"
    echo -e "${YELLOW}üìã –õ–æ–≥–∏ –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä–∞:${NC}"
    ssh -i "$SSH_KEY" -p "$SSH_PORT" "$SERVER_USER@$SERVER_HOST" "docker logs $CONTAINER_NAME"
    exit 1
fi

echo -e "${GREEN}üéâ –î–µ–ø–ª–æ–π –∑–∞–≤–µ—Ä—à–µ–Ω —É—Å–ø–µ—à–Ω–æ!${NC}"
